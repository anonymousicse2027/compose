# ========================= Naive SeedCache =========================
class NaiveSeedCache:
    """Store all ktests per execution, return random seed dir."""
    def __init__(self, root: Path):
        self.root = Path(root).resolve()
        self.root.mkdir(parents=True, exist_ok=True)
        self.entries: List[str] = []

    def store(self, build_idx: int, j: int, outdir: str) -> None:
        src = Path(outdir)
        if not src.exists():
            return
        ktests = list(src.glob("*.ktest"))
        if not ktests:
            return
        seed_dir = self.root / f"b{build_idx}-j{j}"
        seed_dir.mkdir(parents=True, exist_ok=True)
        copied = 0
        for kt in ktests:
            try:
                dst = seed_dir / kt.name
                if not dst.exists():
                    shutil.copy2(str(kt), str(dst))
                    copied += 1
            except Exception:
                pass
        if copied > 0:
            self.entries.append(str(seed_dir))

    def get_random_seed_dir(self) -> Optional[str]:
        if not self.entries:
            return None
        return random.choice(self.entries)

