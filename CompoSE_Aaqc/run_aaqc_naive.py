# ----------------------- utility functions -----------------------
def _escape_sq(s):
    """Escape single quotes for shell."""
    return s.replace("'", "'\"'\"'")


def get_mode_for_prog(prog, cli_mode):
    """CLI mode takes precedence; else per-program default; else 'ere'."""
    if cli_mode:
        return cli_mode
    return get_regex_mode(prog, "ere")


def generate_regex_with_regexgen(mode="ere"):
    """Generate one regex via regexgen and return it as a string."""
    with tempfile.NamedTemporaryFile(mode="r+", delete=False) as tf:
        path = tf.name
    try:
        sp.run(["regexgen", "-c", "1", "-f", path], check=True)
        with open(path, "r") as f:
            for line in f:
                s = line.strip()
                if s:
                    return s
        return ""
    finally:
        try:
            os.remove(path)
        except Exception:
            pass


# ----------------------- src_file: random file pool (from featmaker) -----------------------
def _derive_src_base_dir(prog, pcfg=None):
    """Derive the source-tree base dir from pgm-config (parent of obj-llvm)."""
    if pcfg:
        pd = pcfg.get("pgm_dir", "")
        if pd:
            p = Path(pd).resolve()
            parts = list(p.parts)
            if "obj-llvm" in parts:
                return Path(*parts[:parts.index("obj-llvm")])
            return p.parent
    return None


def _build_all_files_pool(base_dir: Path) -> List[str]:
    """Recursively collect all regular files under base_dir."""
    pool: List[str] = []
    if not base_dir or not base_dir.exists():
        return pool
    try:
        for p in base_dir.rglob("*"):
            try:
                if p.is_file():
                    pool.append(str(p.resolve()))
            except Exception:
                continue
    except Exception:
        return pool
    return pool


# ----------------------- option: --help parsing (from featmaker) -----------------------
_RE_LONG = re.compile(r"--[A-Za-z0-9][A-Za-z0-9\-]*")
_RE_SHORT = re.compile(r"(?<!-)-[A-Za-z]\b")


def _extract_all_flags_from_help(help_text: str) -> List[str]:
    """Extract options from --help text."""
    found: Set[str] = set()
    for ln in help_text.splitlines():
        if "-" not in ln:
            continue
        for m in _RE_LONG.finditer(ln):
            found.add(m.group(0).rstrip(",.;:)=]"))
        for m in _RE_SHORT.finditer(ln):
            found.add(m.group(0).rstrip(",.;:)=]"))
    return sorted(found, key=lambda x: (0 if x.startswith("--") else 1, x))


def _locate_program_binary(prog, pcfg=None):
    """Locate the program binary via pgm-config gcov_path (+ exec_dir), then PATH."""
    candidates = []
    if pcfg:
        gp = pcfg.get("gcov_path", ""); ed = pcfg.get("exec_dir", "").strip("/")
        if gp:
            if ed: candidates.append(os.path.join(gp, ed, prog))
            candidates += [os.path.join(gp, "src", prog), os.path.join(gp, prog)]
    try:
        which = shutil.which(prog)
        if which:
            candidates.append(which)
    except Exception:
        pass
    for c in candidates:
        if c and os.path.isfile(c) and os.access(c, os.X_OK):
            return c
    return None


def _get_help_flag_candidates(prog) -> List[str]:
    """Run <prog> --help and return the list of available option candidates."""
    bin_path = _locate_program_binary(prog, pcfg)
    if not bin_path:
        return []
    try:
        res = sp.run([bin_path, "--help"], stdout=sp.PIPE, stderr=sp.STDOUT,
                     text=True, check=False, timeout=10)
        txt = res.stdout or ""
        return _extract_all_flags_from_help(txt)
    except Exception:
        return []


def _pick_one_random_flag(candidates: List[str]) -> str:
    """Pick one candidate at random; empty string if none."""
    if not candidates:
        return ""
    return random.choice(candidates)


# ----------------------- command builder (from featmaker style) -----------------------
def build_run_args(prog, option_flag, regex, src_file, src_file_2=None):
    """
    Assemble option + regex + src_file per program and return as a string.

    Rules:
    - option: a single randomly chosen flag (may be empty)
    - regex: generated via regexgen
    - src_file: a randomly chosen file path (fixed at program start)
    - diff uses two src_files
    - sed, gawk, csplit use the /regex/ form

    Assembly order: option regex src_file
    """
    q = _escape_sq((regex or "").strip())
    opt = option_flag.strip() if option_flag else ""
    opt_prefix = (opt + " ") if opt else ""

    sf1 = shlex.quote(src_file) if src_file else ""

    # --- per-program assembly ---
    # no hardcoded options; only options randomly chosen from --help are used.
    # only the regex-passing form differs per program.

    if prog == "m4":
        # m4: fixed --warn-macro-sequence invocation (no options, regex only; BRE)
        return f"--warn-macro-sequence='{q}' {sf1}".strip()

    if prog == "diff":
        # diff: option 'regex' src1 src2
        sf2 = shlex.quote(src_file_2) if src_file_2 else sf1
        return f"{opt_prefix}'{q}' {sf1} {sf2}".strip()

    elif prog in ("sed", "gawk", "csplit"):
        # use the /regex/ form
        return f"{opt_prefix}'/{q}/' {sf1}".strip()

    else:
        # everything else: option 'regex' src_file
        return f"{opt_prefix}'{q}' {sf1}".strip()


